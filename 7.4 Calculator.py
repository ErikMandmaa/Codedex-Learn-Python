import math

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def exp(a, b):
    return math.pow(a, b)

print(add(5, 3))
print(subtract(10, 4))
print(multiply(7, 6))
print(divide(20, 5))
print(exp(2, 3))